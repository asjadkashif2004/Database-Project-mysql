use priceoye_pk;
create table Orders(
Order_ID int auto_increment primary key ,
User_ID int not null,
Order_Date DATETIME not null,
Shipping_Address_ID int not null,
Status enum('Order Placed','Order Confirmed','Order Processed','Order Packed','Shipped','In Transit',
'Out for Delivery','Delivered','Delivery Attempted','Returned to Sender','On Hold','Cancelled',
'Refunded','Awaiting Pickup','In Customs') not null default 'Order Placed',
Tracking_ID char(50) unique default(UUID())
);
